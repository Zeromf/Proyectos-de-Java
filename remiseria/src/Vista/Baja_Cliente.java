    private void buscartxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscartxtActionPerformed
        JOptionPane.showMessageDialog(null,"........Tramitando Datos.........");

        String value = valor.getText();
        String key = (String) clave.getSelectedItem();
        Cliente_id cli = new Cliente_id();
        Modelo.Cliente client = (Modelo.Cliente) cli.getDatos(key, value);
        nombre.setText(client.getNombre());
        apellido.setText(client.getApellido());
        Fecha_Nac.setText(datetostring(client.getFechaNaci()));
        DNI.setText(client.getDni());
        direccion.setText(client.getDireccion());
        telefono.setText(Integer.toString(client.getTelefono()));

        JOptionPane.showMessageDialog(null, "Datos Encontrados");


    }//GEN-LAST:event_buscartxtActionPerformed
                             

