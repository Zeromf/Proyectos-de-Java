        int id=Integer.parseInt(valor.getText());

        Cliente_id cli = new Cliente_id();
        Modelo.Cliente b = cli.DeleteCliente(id);
        nombre.setText(b.getNombre());
        apellido.setText(b.getApellido());
        Fecha_Nac.setText(datetostring(b.getFechaNaci()));
